    private void Tabela_de_Produto(){
        Buscas busca = new Buscas();
        
        //passando minha lista retornada do metodo "ListaProduto" para uma nova lista
        List<Produtos> listaProduto = busca.ListaProduto();
        DefaultTableModel tabelaProduto = (DefaultTableModel) tblProduto.getModel();
        
        for(Produtos p : listaProduto){
            Object[] obj = new Object[]{
                p.getCod_barras(),
                p.getNome_produto(),
                p.getValor()
            };
            tabelaProduto.addRow(obj);
        }
    }
