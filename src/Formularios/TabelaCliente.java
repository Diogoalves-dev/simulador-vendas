    private void Tabela_de_Cliente(){
        Buscas busca = new Buscas();
        
        //passando minha lista retornada do metodo "ListaCliente" para uma nova lista
        List<Cliente> listaCliente = busca.ListaCliente();
        DefaultTableModel tabelaCliente = (DefaultTableModel) tblCliente.getModel();
        
        for(Cliente c : listaCliente){
            Object[] obj = new Object[]{
                c.getCpf_cliente(),
                c.getNome_cliente()
            };
            tabelaCliente.addRow(obj);
        }
        
        
    }
